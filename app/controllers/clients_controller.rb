class ClientsController < ApplicationController
  layout "admin"

  before_filter :login_required

  # GET /clients
  # GET /clients.xml
  def index
    logger.info("loading clients")
    @clients = current_user.active_clients
    logger.info("loading inactive clients")
    @inactive_clients = current_user.active_clients(false)

    respond_to do |format|
      format.html # index.html.erb
      format.xml  { render :xml => @clients }
    end
  end

  # GET /clients/1
  # GET /clients/1.xml
  def show
    @client = Client.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.xml  { render :xml => @client }
    end
  end

  # GET /clients/new
  # GET /clients/new.xml
  def new
    @client = Client.new

    respond_to do |format|
      format.html # new.html.erb
      format.xml  { render :xml => @client }
    end
  end

  # GET /clients/1/edit
  def edit
    @client = Client.find(params[:id])
  end

  # POST /clients
  # POST /clients.xml
  def create
    @client = Client.new(params[:client])

    respond_to do |format|
      if @client.save
        flash[:notice] = 'Client was successfully created.'
        format.html { redirect_to(@client) }
        format.xml  { render :xml => @client, :status => :created, :location => @client }
      else
        format.html { render :action => "new" }
        format.xml  { render :xml => @client.errors, :status => :unprocessable_entity }
      end
    end
  end

  # PUT /clients/1
  # PUT /clients/1.xml
  def update
    @client = Client.find(params[:id])

    respond_to do |format|
      if @client.update_attributes(params[:client])
        flash[:notice] = 'Client was successfully updated.'
        format.html { redirect_to :back }
        format.xml  { head :ok }
      else
        format.html { render :action => "edit" }
        format.xml  { render :xml => @client.errors, :status => :unprocessable_entity }
      end
    end
  end

  # DELETE /clients/1
  # DELETE /clients/1.xml
  def destroy
    if current_user.is_admin
      @client = Client.find(params[:id])
      @client.destroy

      respond_to do |format|
        format.html { redirect_to(clients_url) }
        format.xml  { head :ok }
      end
    else
      redirect_to new_session_path
    end
  end

  protected
    def authorized?
      logged_in? && ( current_user.is_admin || ( params[:id] && current_user.is_client_admin?(params[:id]) ) )
    end
end
